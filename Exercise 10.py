# Program to calculate the sum of squares using a for loop

# Initialize the sum variable
sum_of_squares = 0

# Iterate through the numbers from 1 to 5
for num in range(1, 6):

    
    # Calculate the square of each number and add it to the sum
    sum_of_squares += num ** 2

# Print the sum of squares
print("The sum of squares is:", sum_of_squares)
